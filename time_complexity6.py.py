# -------------------------------
# مثال برنامه با پیچیدگی زمانی تقریبی:
# F(n) ≈ 1.3 n**2 + 6 n + 2
# یا تقریب دیگر: 4/3 n**2 + 6 n + 2
# -------------------------------

n = 6  # می‌توانید n را تغییر دهید

# مقداردهی اولیه متغیرها
a = 1
b = 2
c = 3
d = 4
e = 5
f = 6

# حلقه اصلی i
for i in range(n):
    # حلقه تو در تو j با گام 3
    for j in range(0, n, 3):
        a += 1
        b += 2
        c += a
        d += b

    e += c
    f += d
    a += e
    b += f
    c += a

# چاپ نتیجه نهایی برای مشاهده تغییر متغیرها
print(f"a={a}, b={b}, c={c}, d={d}, e={e}, f={f}")