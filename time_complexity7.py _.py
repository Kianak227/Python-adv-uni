# -------------------------------
# مثال: تحلیل پیچیدگی زمانی حلقه while
# -------------------------------

n = 128  # مثال: n = 2**7

# چاپ مقدار اولیه n
print(f"مقدار اولیه n = {n}")

# شمارنده تعداد تکرار حلقه
count = 0

# حلقه ای که n را نصف می کند تا n <= 1 شود
while n > 1:
    n = n // 2  # هر بار n نصف می شود
    count += 1
    print(f"تکرار {count}: n = {n}")

# محاسبه و چاپ پیچیدگی زمانی تابع
F_n = count  # تعداد تکرار برابر log2(n) است
print(f"\nتعداد تکرار حلقه (F(n)) = log2(n) ≈ {F_n}")

# توضیح:
# اگر n = 2**7 = 128 باشد:
# log2(128) = 7
# بنابراین حلقه 7 بار اجرا می شود تا n به 1 برسد