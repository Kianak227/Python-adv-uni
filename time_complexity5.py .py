#
# تعداد تکرارهای حلقه دوم: n + (n-1) + (n-2) + ... + 1 = n*(n+1)/2
# F(n) = 3/2 * n*(n+1) = 1.5*n**2 + 1.5*n
# -------------------------------

n = 5  # مثال، می‌توانید مقدار n را تغییر دهید
p = 10  # مقدار اولیه p

# حلقه اول
for i in range(n):
    # حلقه دوم
    for j in range(i, n):
        k = p
        # چاپ مقادیر برای درک بهتر
        print(f"i={i}, j={j}, k={k}, p={p}")

# محاسبه F(n) طبق فرمول داده شده
F_n = 1.5 * n**2 + 1.5 * n
print(f"\nF({n}) = {F_n}")