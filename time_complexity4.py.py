# فرمول کلی برای محاسبه F(n):
# for i in range(start, stop, step):
#     k = ...
# 
# F(n) = k/step * (stop - start)
# -------------------------------

# -------------------------------
# تمرین مثال:
# حلقه از 10 تا (2*n + 6) با گام 5
# k، p، j در هر تکرار تغییر می‌کنند
# محاسبه F(n) طبق فرمول داده شده
# -------------------------------

n = 5  # مثال، می‌توانید مقدار n را تغییر دهید

# مقداردهی اولیه متغیرها
k = 3
s = 7
l = 2

# حلقه اصلی
for i in range(10, 2*n + 6, 5):
    # ذخیره مقدار قبلی k در p
    p = k
    # به‌روزرسانی k با مقدار s
    k = s
    # به‌روزرسانی j با مقدار l
    j = l
    # چاپ مقادیر برای مشاهده روند تغییر
    print(f"i={i}, p={p}, k={k}, j={j}")

# محاسبه F(n) طبق فرمول تمرین
F_n = (3/5)*(2*n - 4)  # همان F(n) = 1.2*n - 2.4
print(f"\nF({n}) = {F_n}")